package java_basic;

public class Topic_04_Operator {
	public static void main (String[] args) {
		int number = 167679999;
		number += 5; // nghĩa là number = number + 5
		number -= 5; // nghĩa là number = number -5
		number *= 5; // nghĩa là number = number * 5
		number /= 5; // chia lấy phần nguyên
		number %= 5; // chia lấy phần dư
		
		System.out.println(number);
	}
	
}
